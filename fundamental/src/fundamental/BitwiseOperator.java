package fundamental;

public class BitwiseOperator {
	public static void main(String[] args) {
		int a = 10, b= 2;
		System.out.println(a&b);//AND operation is performed at the binary level and the result is produced.
		System.out.println(a|b);//OR operation is performed at the binary level and the result is produced.
		System.out.println(a^b);//XOR operation is performed at the binary level and the result is produced.
		System.out.println(~a);//NOT operation is performed at the binary level and the result is produced.
	}

}
